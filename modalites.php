              <li>
                Détenir un diplôme d'études secondaires DES obtenu au secteur des jeunes ou celui des adultes.*
              </li>
            </ul>

            Remarque : Le titulaire d'un DES obtenu sous un ancien régime de sanction et qui n'aurait pas réussi une ou des matières parmi les suivantes pourrait se voir imposer des activités de mise à niveau :
            
            <ul>
              <li>langue d'enseignement de la 5e secondaire;</li>
              <li>langue seconde de la 5e secondaire;</li>
              <li>mathématiques de la 4e secondaire;</li>
              <li>science et technologie ou applications technologiques et scientifiques de la 4e secondaire (sciences physiques);</li>
              <li>histoire et éducation à la citoyenneté de la 4e secondaire.</li>
            </ul>

