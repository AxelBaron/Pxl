      <h1 id="lesmatieres">Les matières</h1>
        <div class="conteneursecondaire">
          <h2>Première année</h2>
          <div class="conteneur-matieres">

            <div class="mat">
              <h2 id="domaine_du_multimedia">Domaine du multimédia</h2>
              <p>
                L'étudiant se familiarise avec les principaux aspects des métiers de développeur, concepteur, designer et réalisateur multimédia. Il prend connaissance des différents types de produits offerts sur le marché ainsi que des différents types d'entreprises. Il s'initie aux principaux outils utilisés.
              </p>
              
            </div>

            <div class="mat">
              <h2 id="environnement_informatique">Environnement informatique</h2>
              <p>
                L'étudiant prépare son environnement informatique et se familiarise avec les systèmes d'exploitation ainsi qu'avec les réseaux. Il évalue ses besoins en logiciels, gestion de l'information et en ergonomie du poste de travail. Il configure le système d'exploitation et logiciels selon ses besoins.
              </p>
              
            </div>
            
            <div class="mat">
              <h2 id="illustration">Illustration</h2>
              <p>
                L'étudiant apprend les bases du dessin et de l'illustration. Il se familiarise avec des notions telles que la ligne de contour, la perspective, la typographie et la classification des couleurs. Il commence son apprentissage du dessin vectoriel et en comprend l'importance dans la production multimédia.
              </p>
               
            </div>
            <div class="mat">
              <h2 id="conception_de_sites_web">Conception de sites Web</h2>
              <p>
                L'étudiant apprend les bases des langages de balisage et de feuilles de style pour le Web. Il apprend à faire une démarcation entre la structure et la présentation d'un document. À l'aide d'un éditeur, il monte des pages Web selon les constructions les plus courantes.
                <br />
              </p>
               
            </div>
            <div class="mat">
              <h2 id="algorithmique_et_interactivite">Algorithmique et interactivité</h2>
              <p>
                L'étudiant s'initie à la résolution de problèmes, à l'algorithmie et au monde de la programmation.
                Il traduit l'algorithme dans un langage de programmation simple. Il réalise une application multimédia interactive et résout les erreurs de programmation.
              </p>
               
            </div>

            <div class="mat">
              <h2 id="programmation_de_l_interactivite">Programmation de l'interactivité</h2>
              <p>
                L'étudiant poursuit sa logique de programmation et se familiarise avec les différents niveaux d'interactivité dans une production multimédia. Il programme l'interactivité et intègre de façon cohérente les différents médias. Il adapte et vérifie la programmation sur différents fureteurs et plates-formes.
              </p>
              
            </div>

            <div class="mat">
              <h2 id="ergonomie_web">Ergonomie Web</h2>
              <p>
                L’étudiant crée et adapte les contenus et les fonctionnalités d'un site Web en vue d'en faciliter l'utilisation pour ses visiteurs. Il prévoit les réactions des utilisateurs et conçoit des pages Web facilement utilisables. Il saisit l'importance de la normalisation, de l'accessibilité et à la qualité d'un site Web.
              </p>
               
            </div>
            <div class="mat">
              <h2 id="graphisme_et_mise_en_page">Graphisme et mise en page</h2>
              <p>
                L'étudiant aborde la manipulation des images et des textes sous l'angle des traitements matriciels. Il se familiarise avec les périphériques d'entrée. Il crée et optimise les éléments visuels d'une page-écran en vue de leur intégration. Il prend conscience des enjeux reliés à la propriété intellectuelle.
              </p>
              
            </div>
            <div class="mat">
              <h2 id="architecture_de_l_information">Architecture de l'information</h2>
              <p>
                L'étudiant se familiarise avec les principaux outils de recherche, apprend à trier, à classer l'information, à rédiger et à présenter les résultats de ses recherches. Il élabore un scénario non linéaire et tient compte des structures d'organisation du contenu.
              </p>
               
            </div>
            <div class="mat">
              <h2 id="animation">Animation</h2>
              <p>
                L'étudiant se familiarise avec les notions de base en animation. Il réalise des animations simples en utilisant les outils à sa disposition dont il aura appris l'utilisation. Il est introduit aux notions de base du mouvement et à l'écriture d'un scénarimage.
              </p>
            </div>            
          </div>
        </div>
     
      <div class="conteneursecondaire">
          <h2>Deuxième année</h2>
          <div class="conteneur-matieres">
            <div class="mat">
              <h2 id="gestion_de_bases_de_donnees">Gestion de bases de données</h2>
              <p>
                L'étudiant analyse la demande du client et définit les tables de la base de données. Il crée des formulaires de saisi afin de valider rigoureusement les données entrées. Il prépare des requêtes d'extraction de données et produit la mise en page des résultats.
              </p>
              
            </div>
            <div class="mat">
              <h2 id="composition_visuelle">Composition visuelle</h2>
              <p>
                L'étudiant met à profit les notions apprises dans le domaine visuel pour concevoir et réaliser des projets qui tiennent compte des contraintes et des contextes de la communication médiatique. Il est amené à façonner des documents promotionnels sur différents supports.
              </p>
              
            </div>
            <div class="mat">
              <h2 id="scenarisation">Scénarisation</h2>
              <p>
                L'étudiant se familiarise avec le processus d'analyse et de scénarisation d'une production audiovisuelle. Il apprend des notions telles que l'écriture d'un synopsis, d'un scénario linéaire et les différents éléments constitutifs d'une narration. Il fait un survol des techniques de scénarisation interactives.
              </p>
               
            </div>
            <div class="mat">
              <h2 id="conception_sonore">Conception sonore</h2>
              <p>
                L'étudiant se familiarise avec des notions historiques sur la création musicale et sonore. Il développe des habilités à concevoir et à traiter le son. Il comprend l'importance du son dans les domaines de l'audiovisuel et du multimédia. Il réalise des prises de son, du bruitage et de courts montages sonores.
              </p>
               
            </div>
            <div class="mat">
              <h2 id="tournage_video">Tournage vidéo</h2>
              <p>
                L'étudiant se familiarise avec l'analyse, la manipulation et la diffusion de la vidéo numérique. Il développe une connaissance du langage cinématographique et du vocabulaire lié au montage. Il développe des habiletés à créer un discours cohérent à l'aide des techniques de montage.
              </p>
               
            </div>

            <div class="mat">
              <h2 id="langage_serveur_web">Langage serveur Web</h2>
              <p>
                L’étudiant s'initie aux aspects particuliers du développement d'applications pour le Web. Il se familiarise avec un système de gestion de bases de données, avec un langage de programmation serveur et avec dans le but de réaliser une application Web dynamique.
              </p>
              
            </div>

            <div class="mat">
              <h2 id="communication_et_medias">Communication et médias</h2>
              <p>
                L'étudiant explore le domaine de la communication par le biais des médias de masse et des médias interactifs. L'étudiant approfondit les enjeux de la communication dans les domaines du marketing et de la publicité.Il saisit la complexité des enjeux communicationnels et l'importance accordée au discours médiatique.
              </p>
               
            </div>
            <div class="mat">
              <h2 id="design_graphique">Design graphique</h2>
              <p>
                L'étudiant s'initie aux processus de conception des éléments graphiques. Il approfondit les notions traitant de l'organisation picturale, de la typographie, en découvrant les styles et les différents courants esthétiques liés aux arts visuels. Il développe un sens critique fondé sur l'analyse et la connaissance des disciplines de la communication visuelle.
              </p>
              
            </div>
            <div class="mat">
              <h2 id="conception_interactive">Conception interactive</h2>
              <p>
                L'étudiant comprend l'importance de la conception centrée sur l'utilisateur et de l'interactivité dans la production multimédia. Il décrit les interactions entre un utilisateur et une application dans un contexte spécifique. Il approfondit sa connaissance d'un langage de programmation. Il propose des solutions originales et ergonomiques à des problèmes complexes.
              </p>
               
            </div>
            <div class="mat">
              <h2 id="monde_professionnel">Monde professionnel</h2>
              <p>
                L’étudiant apprend à présenter sa personne, sa démarche, son travail et ses aspirations dans les multiples contextes professionnels liés au multimédia. Il analyse la nature, les rouages et les attentes des entreprises dans le but de faire un stage. Il s'initie à la culture entrepreneuriale, aux méthodes de démarrage d'entreprise et au travail autonome. Il réalise un premier portfolio.
              </p>
